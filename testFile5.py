import pytest
from lab04 import solveMaze
from Stack import Stack

def test_no_solution():
    maze = [
        ['+', '+', '+', '+', '+', '+'],
        ['+', '+', '+', '+', '+', '+'],
        ['+', '+', '+', '+', '+', '+'],
        ['+', '+', '+', '+', '+', '+'],
        ['+', '+', '+', '+', '+', '+'],
        ['+', '+', '+', '+', '+', '+']
    ]
    assert solveMaze(maze, 1, 1) == False

def test_open_space_maze():
    maze = [
        ['+', '+', '+', '+', '+', '+'],
        ['+', ' ', ' ', ' ', 'G', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', '+', '+', '+', '+', '+']
    ]
    assert solveMaze(maze, 1, 1) == True

def test_straight_forward_path():
    maze = [
        ['+', '+', '+', '+', '+', '+'],
        ['+', ' ', 'G', '+', '+', '+'],
        ['+', '+', '+', '+', '+', '+'],
        ['+', '+', '+', '+', '+', '+'],
        ['+', '+', '+', '+', '+', '+'],
        ['+', '+', '+', '+', '+', '+']
    ]
    assert solveMaze(maze, 1, 1) == True

